// INCLUDES.
#include "Input.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

// CONSTRUCTORS & DESTRUCTOR.
Input::Input()
{
	// set default values
	mouse.left = false;
	mouse.right = false;
	mouse.x = 0;
	mouse.y = 0;
}

Input::~Input()
{
	//std::cout << "Input object destroyed!\n";
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

// FUCNTIONS.

void Input::setButtonDown(int button)
{
	if (button >= 0)
	{
		buttons[button] = true;
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setButtonUp(int button)
{
	if (button >= 0)
	{
		buttons[button] = false;
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Input::isButtonDown(int button)
{
	if (button >= 0)
	{
		return buttons[button];
	}

	return false;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setLeftStick(float xAxis, float yAxis)
{
	// This was implemented to take care of controller drift and the fact
	// that the built in function window.setJoystickThreshold(n) doesn't
	// seem to do anything. Drift for my controller was still occurring
	// at 3.0f, and had stopped at 4.0f, but has been set to 6.0f just to be safe
	float threshold = 20.0f;

	if (abs(xAxis) < threshold)
	{
		leftStickPos.x = 0.0f;
	}
	else
	{
		leftStickPos.x = xAxis;
	}

	if (abs(yAxis) < threshold)
	{
		leftStickPos.y = 0.0f;
	}
	else
	{
		leftStickPos.y = yAxis;
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

sf::Vector2f Input::getLeftStick()
{
	return leftStickPos;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMouseX(int lx)
{
	mouse.x = lx;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMouseY(int ly)
{
	mouse.y = ly;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMousePosition(int lx, int ly)
{
	setMouseX(lx);
	setMouseY(ly);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Input::getMouseX()
{
	return mouse.x;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Input::getMouseY()
{
	return mouse.y;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMouseLDown(bool down)
{
	mouse.left = down;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Input::isMouseLDown()
{
	return mouse.left;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Input::setMouseRDown(bool down)
{
	mouse.right = down;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Input::isMouseRDown()
{
	return mouse.right;
}

